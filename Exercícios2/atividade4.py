#algoritmo que calcula a media final na unisinos

#ler grau A e grau B
grau_A = float(input('Digite a sua nota do grau A: '))
grau_B = float(input('Digite a sua nota do grau B: '))

#calcular a média final
media_final = (grau_A * 1/3) + (grau_B * 2/3)

#informar a média final
print('A média final é: ', media_final)
